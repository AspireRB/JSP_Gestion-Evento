package domain;

import java.util.Date;

public class Evento {
    
    private int id;
    private String nombre, ciudad, lugar, descripcion;
    private Date fechaInicio, fechaFin, horaInicio, horaFin;
    private Double costo;
    private Enum estado;

    public Evento() {
    }
       
    public Evento(int id, String nombre, String ciudad, String lugar, String descripcion, Date fechaInicio, Date fechaFin, Date horaInicio, Date horaFin, Double costo, Enum estado) {
        this.id = id;
        this.nombre = nombre;
        this.ciudad = ciudad;
        this.lugar = lugar;
        this.descripcion = descripcion;
        this.fechaInicio = fechaInicio;
        this.fechaFin = fechaFin;
        this.horaInicio = horaInicio;
        this.horaFin = horaFin;
        this.costo = costo;
        this.estado = estado;
    }

    public Evento(String nombre, String ciudad, String lugar, String descripcion, Date fechaInicio, Date fechaFin, Date horaInicio, Date horaFin, Double costo, Enum estado) {
        this.nombre = nombre;
        this.ciudad = ciudad;
        this.lugar = lugar;
        this.descripcion = descripcion;
        this.fechaInicio = fechaInicio;
        this.fechaFin = fechaFin;
        this.horaInicio = horaInicio;
        this.horaFin = horaFin;
        this.costo = costo;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getLugar() {
        return lugar;
    }

    public void setLugar(String lugar) {
        this.lugar = lugar;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public Date getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(Date fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public Date getFechaFin() {
        return fechaFin;
    }

    public void setFechaFin(Date fechaFin) {
        this.fechaFin = fechaFin;
    }

    public Date getHoraInicio() {
        return horaInicio;
    }

    public void setHoraInicio(Date horaInicio) {
        this.horaInicio = horaInicio;
    }

    public Date getHoraFin() {
        return horaFin;
    }

    public void setHoraFin(Date horaFin) {
        this.horaFin = horaFin;
    }

    public Double getCosto() {
        return costo;
    }

    public void setCosto(Double costo) {
        this.costo = costo;
    }

    public Enum getEstado() {
        return estado;
    }

    public void setEstado(Enum estado) {
        this.estado = estado;
    }
        
}